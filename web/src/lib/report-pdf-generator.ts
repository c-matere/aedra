// --- Shared Helpers ---

const loadImage = (url: string): Promise<HTMLImageElement | null> => {
    return new Promise((resolve) => {
        const img = new Image();
        const timeout = setTimeout(() => {
            console.error(`[PDF Gen] Logo load timeout for: ${url}`);
            resolve(null);
        }, 5000); // 5s timeout

        img.crossOrigin = "anonymous";
        img.onload = () => {
            clearTimeout(timeout);
            resolve(img);
        };
        img.onerror = (err) => {
            clearTimeout(timeout);
            console.error(`[PDF Gen] Logo load error for: ${url}`, err);
            resolve(null);
        };
        img.src = url;
    });
};

const resizeImage = (img: HTMLImageElement, maxDim: number = 800): string | HTMLImageElement => {
    const width = img.width;
    const height = img.height;

    if (width <= maxDim && height <= maxDim) return img;

    const canvas = document.createElement('canvas');
    let newWidth, newHeight;

    if (width > height) {
        newWidth = maxDim;
        newHeight = (height / width) * maxDim;
    } else {
        newHeight = maxDim;
        newWidth = (width / height) * maxDim;
    }

    canvas.width = newWidth;
    canvas.height = newHeight;
    const ctx = canvas.getContext('2d');
    if (!ctx) return img;

    ctx.drawImage(img, 0, 0, newWidth, newHeight);
    return canvas.toDataURL('image/jpeg', 0.85); 
};

async function drawBrandingHeader(
    doc: jsPDF,
    company: CompanyRecord | null,
    title: string,
    meta: { property: string; landlord: string; month: string },
    margin: number,
    rightAlignX: number
): Promise<number> {
    const logoUrl = getLogoUrl(company?.logo) || "/aedra-logo.png";
    const logo = await loadImage(logoUrl);
    if (logo) {
        const optimizedLogo = resizeImage(logo, 600);
        doc.addImage(optimizedLogo, 'JPEG', margin, margin, 25, 25, undefined, 'FAST');
    }

    const startY = 45; 
    doc.setFontSize(9);
    doc.setTextColor(40, 40, 40);
    doc.setFont("helvetica", "bold");
    doc.text(company?.name || "AEDRA MANAGEMENT", margin, startY);
    
    doc.setFont("helvetica", "normal");
    doc.setFontSize(8);
    doc.setTextColor(100, 100, 100);
    
    let currentInfoY = startY + 4;
    const address = company?.address || "P.O BOX 80000-80100, MOMBASA, KENYA";
    doc.text(address, margin, currentInfoY);
    currentInfoY += 4;

    const phone = company?.phone || "Property Management Office";
    doc.text(`TEL: ${phone}`, margin, currentInfoY);
    currentInfoY += 4;

    const email = company?.email || "support@aedra.co.ke";
    doc.text(`EMAIL: ${email}`, margin, currentInfoY);
    currentInfoY += 4;

    if (company?.pinNumber) {
        doc.setFont("helvetica", "bold");
        doc.text(`PIN: ${company.pinNumber}`, margin, currentInfoY);
        currentInfoY += 4;
    }

    const headerFinalY = Math.max(currentInfoY, startY + 20);

    doc.setFontSize(16);
    doc.setTextColor(0, 0, 0);
    doc.setFont("helvetica", "bold");
    doc.text(title, rightAlignX, margin + 5, { align: 'right' });
    
    doc.setFontSize(9);
    doc.setFont("helvetica", "normal");
    doc.text(`PROPERTY: ${meta.property.toUpperCase()}`, rightAlignX, margin + 12, { align: 'right' });
    doc.text(`LANDLORD: ${meta.landlord.toUpperCase()}`, rightAlignX, margin + 17, { align: 'right' });
    doc.text(`MONTH: ${meta.month.toUpperCase()}`, rightAlignX, margin + 22, { align: 'right' });

    return headerFinalY;
}

function drawFooter(doc: jsPDF) {
    const pageCount = (doc as any).internal.getNumberOfPages();
    for (let i = 1; i <= pageCount; i++) {
        doc.setPage(i);
        doc.setFontSize(8);
        doc.setTextColor(150, 150, 150);
        doc.text(
            `Page ${i} of ${pageCount} | Generated by Aedra AI Management System`,
            doc.internal.pageSize.width / 2,
            doc.internal.pageSize.height - 10,
            { align: 'center' }
        );
    }
}

